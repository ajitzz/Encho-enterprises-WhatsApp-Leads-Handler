#!/usr/bin/env node
const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'docs', 'contract-catalog.md');

const read = (rel) => fs.readFileSync(path.join(repoRoot, rel), 'utf8');

const internal = read('backend/shared/contracts/internalEvents.js');
const schemaVersion = internal.match(/SCHEMA_VERSION\s*=\s*['"]([^'"]+)['"]/)?.[1] || 'unknown';
const eventTypeMatches = [...internal.matchAll(/([A-Z_]+_V1):\s*['"]([^'"]+)['"]/g)];

const leadContracts = read('backend/modules/lead-ingestion/contracts.js');
const reminderContracts = read('backend/modules/reminders-escalations/contracts.js');

const pullRequiredFields = (source, symbol) => {
  const regex = new RegExp(`${symbol}[\\s\\S]*?requiredFields:\\s*\\[([^\\]]+)\\]`, 'm');
  const match = source.match(regex);
  if (!match) return [];
  return match[1]
    .split(',')
    .map((v) => v.replace(/[\s'"\n]/g, ''))
    .filter(Boolean);
};

const lines = [];
lines.push('# Contract Catalog');
lines.push('');
lines.push(`Generated by: \`node scripts/generate-contract-catalog.js\``);
lines.push(`Schema baseline: **${schemaVersion}**`);
lines.push('');
lines.push('## Internal Event Types');
for (const [, key, value] of eventTypeMatches) {
  lines.push(`- \`${key}\` => \`${value}\``);
}
lines.push('');
lines.push('## Module Contract Snapshots');
lines.push('');
lines.push('### lead-ingestion');
lines.push(`- Contract: \`LEAD_INGESTED_CONTRACT\``);
lines.push(`- Required fields: ${pullRequiredFields(leadContracts, 'LEAD_INGESTED_CONTRACT').map((f) => `\`${f}\``).join(', ')}`);
lines.push('');
lines.push('### reminders-escalations');
lines.push(`- Contract: \`REMINDER_TASK_CREATED_CONTRACT\``);
lines.push(`- Required fields: ${pullRequiredFields(reminderContracts, 'REMINDER_TASK_CREATED_CONTRACT').map((f) => `\`${f}\``).join(', ')}`);
lines.push(`- Contract: \`REMINDER_TASK_DISPATCHED_CONTRACT\``);
lines.push(`- Required fields: ${pullRequiredFields(reminderContracts, 'REMINDER_TASK_DISPATCHED_CONTRACT').map((f) => `\`${f}\``).join(', ')}`);
lines.push('');

fs.writeFileSync(outputPath, `${lines.join('\n')}\n`, 'utf8');
console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
