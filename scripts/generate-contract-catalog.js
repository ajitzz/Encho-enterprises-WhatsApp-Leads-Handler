#!/usr/bin/env node
const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'docs', 'contract-catalog.md');

const read = (rel) => fs.readFileSync(path.join(repoRoot, rel), 'utf8');

const internal = read('backend/shared/contracts/internalEvents.js');
const schemaVersion = internal.match(/SCHEMA_VERSION\s*=\s*['"]([^'"]+)['"]/)?.[1] || 'unknown';
const eventTypeMatches = [...internal.matchAll(/([A-Z_]+_V1):\s*['"]([^'"]+)['"]/g)];

const moduleContracts = [
  { name: 'lead-ingestion', path: 'backend/modules/lead-ingestion/contracts.js' },
  { name: 'reminders-escalations', path: 'backend/modules/reminders-escalations/contracts.js' },
  { name: 'lead-lifecycle', path: 'backend/modules/lead-lifecycle/contracts.js' },
  { name: 'reporting-export', path: 'backend/modules/reporting-export/contracts.js' },
  { name: 'media', path: 'backend/modules/media/contracts.js' },
  { name: 'bot-conversation', path: 'backend/modules/bot-conversation/contracts.js' },
  { name: 'agent-workspace', path: 'backend/modules/agent-workspace/contracts.js' },
  { name: 'campaign-broadcast', path: 'backend/modules/campaign-broadcast/contracts.js' },
  { name: 'system-health', path: 'backend/modules/system-health/contracts.js' },
  { name: 'auth-config', path: 'backend/modules/auth-config/contracts.js' },
];

const pullRequiredFields = (source, symbol) => {
  const regex = new RegExp(`${symbol}[\\s\\S]*?requiredFields:\\s*\\[([^\\]]+)\\]`, 'm');
  const match = source.match(regex);
  if (!match) return [];
  return match[1]
    .split(',')
    .map((v) => v.replace(/[\s'"\n]/g, ''))
    .filter(Boolean);
};

const pullSchemaConstants = (source) => [...source.matchAll(/([A-Z_]+SCHEMA_VERSION)\s*=\s*['"]([^'"]+)['"]/g)];
const pullValidators = (source) => [...source.matchAll(/function\s+(validate[A-Za-z0-9_]+)\(/g)].map((m) => m[1]);
const pullContractSymbols = (source) => [...source.matchAll(/const\s+([A-Z0-9_]+_CONTRACT)\s*=\s*{/g)].map((m) => m[1]);

const lines = [];
lines.push('# Contract Catalog');
lines.push('');
lines.push('Generated by: `node scripts/generate-contract-catalog.js`');
lines.push(`Schema baseline: **${schemaVersion}**`);
lines.push('');
lines.push('## Internal Event Types');
for (const [, key, value] of eventTypeMatches) {
  lines.push(`- \`${key}\` => \`${value}\``);
}
lines.push('');
lines.push('## Module Contract Snapshots');
lines.push('');

for (const entry of moduleContracts) {
  const source = read(entry.path);
  lines.push(`### ${entry.name}`);

  const schemaConstants = pullSchemaConstants(source);
  if (schemaConstants.length > 0) {
    for (const [, key, value] of schemaConstants) {
      lines.push(`- Schema marker: \`${key}\` = \`${value}\``);
    }
  } else {
    lines.push('- Schema marker: uses shared baseline schema marker');
  }

  const contractSymbols = pullContractSymbols(source);
  if (contractSymbols.length > 0) {
    for (const symbol of contractSymbols) {
      const requiredFields = pullRequiredFields(source, symbol);
      lines.push(`- Contract: \`${symbol}\``);
      lines.push(`- Required fields: ${requiredFields.length > 0 ? requiredFields.map((f) => `\`${f}\``).join(', ') : 'n/a'}`);
    }
  }

  const validators = pullValidators(source);
  lines.push(`- Validators: ${validators.length > 0 ? validators.map((v) => `\`${v}\``).join(', ') : 'n/a'}`);
  lines.push('');
}

fs.writeFileSync(outputPath, `${lines.join('\n')}\n`, 'utf8');
console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
